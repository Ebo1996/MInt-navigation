// ── Search function ──────────────────────────────────────────────────────
export const searchDepartments = (query) => {
  const q = query.toLowerCase();
  return departmentsData.filter((d) => {
    const name = (d.name || "").toLowerCase();
    const desc = (d.description || "").toLowerCase();
    const head = (d.head || "").toLowerCase();
    const wing = (d.wing || "").toLowerCase();
    return name.includes(q) || desc.includes(q) || head.includes(q) || wing.includes(q);
  });
};

// ── Get departments by sector ────────────────────────────────────────────
export const getDepartmentsBySector = (sectorId) =>
  departmentsData.filter((d) => d.sectorId === sectorId);

// ── Get wings for a sector ───────────────────────────────────────────────
export const getWingsBySector = (sectorId) => {
  const depts = getDepartmentsBySector(sectorId);
  return [...new Set(depts.map((d) => d.wing))];
};

// ── Get department by ID ─────────────────────────────────────────────────
export const getDepartmentById = (id) => {
  const d = departmentsData.find((d) => d.id === parseInt(id));
  if (!d) return null;
  return { ...d, name: d.name || "", description: d.description || "" };
};
